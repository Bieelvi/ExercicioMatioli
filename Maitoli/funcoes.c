int somaInt (int a, int b){
    int c;

    c = a + b;

    return c;
}

float perimetro (float a, float b){
    float c;

    c = a + a + b + b;

    return c;
}

float area(float a, float b){
    float c;

    c = a * b;

    return c;
}

float diagonal(float a, float b){
    float c;

    c = sqrt((a*a)+(b*b));

    return c;
}
